package datatypes;


public class EstudandoFloat {
    public static void main(String[] args) {
// Stores whole numbers from -2,147,483,648 to 2,147,483,647 and
        //Stores fractional numbers. Sufficient for storing 6 to 8 decimal digits
        float variavelTipoFloat = 1837383.39383393f;

        System.out.println(variavelTipoFloat);

        variavelTipoFloat = -193838383.88887f;

        System.out.println(variavelTipoFloat);

        variavelTipoFloat = (float) 1191918338.4445555f;

        System.out.println(variavelTipoFloat);

    }

}


